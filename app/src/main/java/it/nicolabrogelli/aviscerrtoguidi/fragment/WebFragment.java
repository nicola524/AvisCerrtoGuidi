    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        View view = inflater.inflate(R.layout.fragment_web, container, false);
        WebView webView = (WebView) view.findViewById(R.id.webView_Site);
        final ProgressBar progressBar  = (ProgressBar) view.findViewById(R.id.progressBar1);

        webView.clearHistory();
        webView.clearFormData();
        webView.clearCache(true);
        webView.setBackgroundColor(Color.parseColor("#c51313"));
        webView.getSettings().setJavaScriptEnabled(true);
        webView.getSettings().setDatabaseEnabled(true);
        webView.getSettings().setDomStorageEnabled(true);
        webView.getSettings().setGeolocationEnabled(true);
        //webView.getSettings().setGeolocationDatabasePath(getFilesDir().getPath());
        webView.setWebChromeClient(new WebChromeClient() {

            @Override
            public void onProgressChanged(WebView view, int newProgress) {

            }

        });

        webView.setWebViewClient(new WebViewClient() {

            @Override
            public void onPageStarted(WebView view, String url, Bitmap favicon) {
                super.onPageStarted(view, url, favicon);

            }

            @Override
            public boolean shouldOverrideUrlLoading(WebView view, String url) {

                //webView.setVisibility(View.GONE);
                progressBar.setVisibility(View.VISIBLE);

                view.loadUrl(url);

                return true;

            }

            @Override
            public void onPageFinished(WebView view, String url) {
                super.onPageFinished(view, url);

                progressBar.setVisibility(View.GONE);
                //webView.setVisibility(View.VISIBLE);

            }

            @Override
            public void onReceivedError(WebView view, int errorCode,
                                        String description, String failingUrl) {
                if (view.canGoBack()) {
                    view.goBack();
                }
                /*
                Toast toast = Toast.makeText(getBaseContext(), description,
                        Toast.LENGTH_SHORT);
                toast.setGravity(Gravity.TOP | Gravity.CENTER, 0, 0);
                toast.show();*/
            }


        });

        webView.loadUrl("http://www.aviscerretoguidi.it");

        return view;
    }

